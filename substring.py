#Counting starts from 0
sentance = "0123456789abcdef"
print(sentance)

#1st
print(sentance[0])

#2nd to last
print(sentance[-2])

#from 3rd to 9th
print(sentance[2:9])

#first 9
print(sentance[:9])

#from -9th to -2nd
print(sentance[-9:-2])

#every third starting from 3rd to 9th
print(sentance[2:9:3])

#every third
print(sentance[::3])

#reverse
print(sentance[::-1])